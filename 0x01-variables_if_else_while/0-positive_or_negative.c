#include <stdio.h>
/**
 * Main - Entry point
 * function - Assign random number to n
 *
 * Return: Always 0 (if successful)
 */

int main(void)
{
	int n;

	if (n > 0)
	{
		printf("The number is positive \n");
	}
	else if (n == 0)
	{
		printf("The number is zero \n");
	}
	else 
	{
		printf("The number is negative \n");
	}
	return (0);
}
